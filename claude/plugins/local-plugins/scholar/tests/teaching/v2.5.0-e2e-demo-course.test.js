// F3: Section Refinement Tests
// ═══════════════════════════════════════════════════════════

describe('F3: --refine (Demo course validation)', () => {
  it('should parse sections from demo lecture', () => {
    const testFile = join(LECTURES_DIR, 'week03-linear-regression.qmd');

    if (!existsSync(testFile)) {
      return;
    }

    const content = readFileSync(testFile, 'utf-8');
    const parsed = parseQmdContent(content);

    expect(parsed.sections).toBeTruthy();
    expect(parsed.sections.length).toBeGreaterThan(0);

    // Each section should have title, level, content
    parsed.sections.forEach((section) => {
      expect(section).toHaveProperty('title');
      expect(section).toHaveProperty('level');
      expect(section).toHaveProperty('content');
      expect(typeof section.title).toBe('string');
      expect(typeof section.level).toBe('number');
    });
  });

  it('should identify section boundaries correctly', () => {
    const testFile = join(LECTURES_DIR, 'week03-linear-regression.qmd');

    if (!existsSync(testFile)) {
      return;
    }

    const content = readFileSync(testFile, 'utf-8');
    const parsed = parseQmdContent(content);

    // Sections should not overlap
    for (let i = 0; i < parsed.sections.length - 1; i++) {
      const currentSection = parsed.sections[i];
      const nextSection = parsed.sections[i + 1];

      // Current section should not contain next section's heading
      expect(currentSection.content).not.toContain(`## ${nextSection.title}`);
    }
  });
});

