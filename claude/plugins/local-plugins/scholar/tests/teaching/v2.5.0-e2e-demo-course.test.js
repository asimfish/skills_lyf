// Helper: Parse YAML frontmatter from QMD
function parseFrontmatter(qmdPath) {
  const content = readFileSync(qmdPath, 'utf-8');
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return null;
  return match[1];
}

