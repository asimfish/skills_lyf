describe('F1: --output-dir (Unit-level validation)', () => {
  it('should generate correct filename for topic', () => {
    const filename = generateLectureFilename({ topic: 'Correlation Analysis' });
    expect(filename).toBe('lecture-correlation-analysis.qmd');
  });

  it('should use week prefix when fromPlan provided', () => {
    const filename = generateLectureFilename({
      topic: 'Correlation Analysis',
      fromPlan: 'week04'
    });
    expect(filename).toBe('week04-correlation-analysis.qmd');
  });

  it('should handle spaces in topic', () => {
    const filename = generateLectureFilename({ topic: 'Multiple Linear Regression' });
    expect(filename).toBe('lecture-multiple-linear-regression.qmd');
  });

  it('should truncate long topics to 80 chars', () => {
    const longTopic = 'A'.repeat(100);
    const filename = generateLectureFilename({ topic: longTopic });

    // Extract slug from filename (remove lecture- prefix and .qmd suffix)
    const slug = filename.replace('lecture-', '').replace('.qmd', '');
    expect(slug.length).toBeLessThanOrEqual(80);
  });

  it('should sanitize special characters', () => {
    const filename = generateLectureFilename({ topic: 'R² & ANOVA (p < 0.05)' });
    expect(filename).not.toContain('²');
    expect(filename).not.toContain('&');
    expect(filename).not.toContain('<');
    expect(filename).toMatch(/^lecture-[a-z0-9-]+\.qmd$/);
  });
});

